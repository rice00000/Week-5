# int() function
# Example of calling int()
whole_number = int("123")

# Name: int
# Arguments: A value to convert to an integer (here, the string "123").
# Side Effects: None.
# Return Value: The integer version of the value (123).



# str() function
# Example of calling str()
text_value = str(123)

# Name: str
# Arguments: A value to convert to a string (here, 123).
# Side Effects: None.
# Return Value: The string version of the value ("123").



# bool() function
# Example of calling bool()
truth_value = bool(None)

# Name: bool
# Arguments: A value to test for truthiness (here, None).
# Side Effects: None.
# Return Value: True if the value is truthy; False for falsy values such as 0 or None.



# float() function
# Example of calling float()
decimal_number = float("1.23")

# Name: float
# Arguments: A value to convert to a floating-point number (here, "1.23").
# Side Effects: None.
# Return Value: The float version of the value (1.23).



# len() function
# Example of calling len()
length_of_word = len("python")

# Name: len
# Arguments: A sequence or collection (here, the string "python").
# Side Effects: None.
# Return Value: The number of items in the object (6).



# pow() function
# Example of calling pow()
power_result = pow(1, 23)

# Name: pow
# Arguments: Base and exponent (here, 1 and 23).
# Side Effects: None.
# Return Value: The base raised to the exponent (1).



# abs() function
# Example of calling abs()
absolute_value = abs(-123)

# Name: abs
# Arguments: A number (here, -123).
# Side Effects: None.
# Return Value: The absolute (non-negative) value (123).

